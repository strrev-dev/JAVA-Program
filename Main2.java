//How to print different type of values in Java?
public class Main2{

    public static void main(String[] args) {

        System.out.println("Printing different type of values :");

        int num = 234;
        float  b;
         char  c;
         String s;

         b = 34.83f;
          c = 'A';
          s ="hello world";

          System.out.println("value of integer =" + num );
          System.out.println("value of float =" + b);
          System.out.println("value of character = " + c);
          System.out.println("value of String = " + s );

    }
}

